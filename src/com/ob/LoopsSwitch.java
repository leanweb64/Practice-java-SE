package com.ob;

public class LoopsSwitch {
    public static void main(String[] args) {
        int num = 1;
        switch (num){
            case 5:
                System.out.println("Uno");
                    break;
                case 2:
                System.out.println("Dos");
                    break;
            case 3:
                System.out.println("Tres");
                    break;
            default:
                System.out.println("Ningun numero anterior");

        }
    }
}
